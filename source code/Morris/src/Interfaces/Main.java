    public void verVictoria(){
        switch (verficarFichaV()) {
            case 2:
                interfazVictoria("Ha ganado las blancas");
                break;
            case 3:
                interfazVictoria("Ha ganado las  negras");
                break;
            default:
                System.out.println("QUE SIGA EL JUEGO");
                break;
        }
    }
    
    public int verficarFichaV(){
        int cN=0,cB=0;
        for (int i = 0; i < 9; i++) {
            if (MaquinasBlancas[i].getX()==0&&MaquinasBlancas[i].getY()==0) {
                cB++;
            }
            if (MaquinasNegras[i].getX()==0&&MaquinasNegras[i].getY()==0) {
                cN++;
            }
        }
        
        if (cB==7) {
            return 3;
        }else if(cN==7){
            return 2;
        }
        return 0;
    }
    
